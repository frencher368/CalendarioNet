    private void jButton32ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton32ActionPerformed
        
        //-- Extrae los datos registrados en los campos --//
        String nombre = JNombre.getText();
        String hora = JHora.getText();
        String descripcion = JDes.getText();
        String dia = JDia.getText();
        
        //-- Crea el objeto calendario con sus respectivos datos --//
        CalendarioNet Calen = new CalendarioNet(nombre, descripcion, hora, dia);
        
        //-- Se guarda el calendario al contenedor --//
        Contenedor.add(Calen);
        
        //-- Se limpian los campos --//
        JNombre.setText("");
        JHora.setText("");
        JDia.setText("");
        JDes.setText("");
        
    }//GEN-LAST:event_jButton32ActionPerformed

