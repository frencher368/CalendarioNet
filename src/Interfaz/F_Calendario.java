    private void jButton33ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton33ActionPerformed
        
        String nombre = JNombre.getText();
        String des = JDes.getText();
        String hora = JHora.getText();
        String dia = JDia.getText();
        
        CalendarioNet Calen = new CalendarioNet(nombre, des, hora, dia);
        Contenedor.set(Encontrado, Calen);
        
        //-- Se limpian los campos --//
        JNombre.setText("");
        JHora.setText("");
        JDia.setText("");
        JDes.setText("");
        
    }//GEN-LAST:event_jButton33ActionPerformed

    private void JBusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_JBusActionPerformed
        
        String  codigo = JNombre.getText().trim();
            
            CalendarioNet p;
            for (int i=0; i<Contenedor.size(); i++)
                {
                    p = (CalendarioNet) Contenedor.get(i);
                    if (codigo.equals(p.getNombre_del_evento()))
                        {
                            JHora.setText(p.getHora_del_evento());
                            JDia.setText(p.getDia_del_evento());
                            JDes.setText(p.getDescripcion_del_evento());
                            Encontrado = i;
                            break;
                        }
                }
        
    }//GEN-LAST:event_JBusActionPerformed

